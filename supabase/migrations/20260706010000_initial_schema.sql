create table if not exists public.tb_usuarios (
  id_usuario bigint generated by default as identity primary key,
  auth_user_id uuid unique references auth.users(id) on delete set null,
  nome_usuario varchar(60) not null,
  matricula varchar(14) not null unique,
  status varchar(12) not null default 'ativo' check (status in ('ativo', 'inativo')),
  perfil smallint not null default 0 check (perfil in (0, 1, 2)),
  email varchar(120) not null unique,
  usuario varchar(60) unique,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.tb_dispositivos_eletronicos (
  id_produto bigint generated by default as identity primary key,
  descricao varchar(60) not null unique,
  pontuacao integer not null check (pontuacao > 0),
  status varchar(15) not null default 'ativo' check (status in ('ativo', 'inativo')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.tb_transacao (
  id_transacao bigint generated by default as identity primary key,
  data date not null default current_date,
  quantia integer not null check (quantia > 0),
  pontos integer not null check (pontos >= 0),
  status_transacao varchar(13) not null default 'ativo' check (status_transacao in ('ativo', 'cancelado')),
  desc_modelo varchar(100) not null,
  id_produto_fk bigint not null references public.tb_dispositivos_eletronicos(id_produto),
  id_usuario_fk bigint not null references public.tb_usuarios(id_usuario),
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now()
);

create table if not exists public.tb_pontuacao (
  id_pontuacao bigint generated by default as identity primary key,
  pontuacao integer not null default 0 check (pontuacao >= 0),
  data date not null default current_date,
  ano integer not null check (ano between 2000 and 2100),
  mes integer not null check (mes between 1 and 12),
  id_usuario_tb_fk bigint not null references public.tb_usuarios(id_usuario) on delete cascade,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (id_usuario_tb_fk, ano, mes)
);

create table if not exists public.auditoria (
  id bigint generated by default as identity primary key,
  usuario_id varchar(70),
  cpf varchar(14),
  acao varchar(15),
  tabela varchar(70),
  data_acao date not null default current_date,
  hora_acao time not null default localtime,
  antes_acao text,
  depois_acao text,
  actor_auth_user_id uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now()
);

create index if not exists tb_usuarios_auth_user_id_idx on public.tb_usuarios(auth_user_id);
create index if not exists tb_usuarios_perfil_idx on public.tb_usuarios(perfil);
create index if not exists tb_transacao_usuario_data_idx on public.tb_transacao(id_usuario_fk, data desc);
create index if not exists tb_transacao_produto_idx on public.tb_transacao(id_produto_fk);
create index if not exists tb_pontuacao_ranking_idx on public.tb_pontuacao(ano, mes, pontuacao desc);

