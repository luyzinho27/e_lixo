create or replace function public.current_profile_id()
returns bigint
language sql
stable
security definer
set search_path = public
as $$
  select id_usuario
  from public.tb_usuarios
  where auth_user_id = auth.uid()
  limit 1;
$$;

create or replace function public.current_perfil()
returns smallint
language sql
stable
security definer
set search_path = public
as $$
  select perfil
  from public.tb_usuarios
  where auth_user_id = auth.uid()
  limit 1;
$$;

create or replace function public.is_staff()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(public.current_perfil() in (1, 2), false);
$$;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(public.current_perfil() = 2, false);
$$;

create or replace view public.v_ranking_mensal
as
select
  p.id_usuario_tb_fk as id_usuario,
  u.nome_usuario,
  p.ano,
  p.mes,
  p.pontuacao,
  make_date(p.ano, p.mes, 1) as mes_referencia
from public.tb_pontuacao p
join public.tb_usuarios u on u.id_usuario = p.id_usuario_tb_fk
where p.pontuacao > 0
  and u.status = 'ativo';

create or replace function public.registrar_transacao(
  p_id_produto bigint,
  p_id_usuario bigint,
  p_quantia integer,
  p_data date,
  p_desc_modelo text,
  p_status text default 'ativo'
)
returns table (
  id_transacao bigint,
  pontos integer
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_actor_id bigint;
  v_actor_nome text;
  v_actor_cpf text;
  v_actor_perfil smallint;
  v_produto record;
  v_total integer;
  v_year integer;
  v_month integer;
begin
  if auth.uid() is null then
    raise exception 'Usuario nao autenticado.';
  end if;

  select id_usuario, nome_usuario, matricula, perfil
  into v_actor_id, v_actor_nome, v_actor_cpf, v_actor_perfil
  from public.tb_usuarios
  where auth_user_id = auth.uid()
    and status = 'ativo';

  if v_actor_id is null then
    raise exception 'Perfil do usuario nao encontrado.';
  end if;

  if v_actor_perfil = 0 and p_id_usuario <> v_actor_id then
    raise exception 'Participantes so podem registrar transacoes para si mesmos.';
  end if;

  if p_quantia is null or p_quantia <= 0 then
    raise exception 'A quantidade deve ser maior que zero.';
  end if;

  select id_produto, descricao, pontuacao
  into v_produto
  from public.tb_dispositivos_eletronicos
  where id_produto = p_id_produto
    and status = 'ativo';

  if v_produto.id_produto is null then
    raise exception 'Dispositivo nao encontrado ou inativo.';
  end if;

  if not exists (
    select 1
    from public.tb_usuarios
    where id_usuario = p_id_usuario
      and status = 'ativo'
  ) then
    raise exception 'Participante nao encontrado ou inativo.';
  end if;

  v_total := v_produto.pontuacao * p_quantia;
  v_year := extract(year from p_data)::integer;
  v_month := extract(month from p_data)::integer;

  insert into public.tb_transacao (
    data,
    quantia,
    pontos,
    status_transacao,
    desc_modelo,
    id_produto_fk,
    id_usuario_fk,
    created_by
  )
  values (
    p_data,
    p_quantia,
    v_total,
    coalesce(p_status, 'ativo'),
    left(p_desc_modelo, 100),
    p_id_produto,
    p_id_usuario,
    auth.uid()
  )
  returning tb_transacao.id_transacao, tb_transacao.pontos
  into id_transacao, pontos;

  insert into public.tb_pontuacao (
    pontuacao,
    data,
    ano,
    mes,
    id_usuario_tb_fk
  )
  values (
    v_total,
    p_data,
    v_year,
    v_month,
    p_id_usuario
  )
  on conflict (id_usuario_tb_fk, ano, mes)
  do update set
    pontuacao = public.tb_pontuacao.pontuacao + excluded.pontuacao,
    data = excluded.data,
    updated_at = now();

  insert into public.auditoria (
    usuario_id,
    cpf,
    acao,
    tabela,
    depois_acao,
    actor_auth_user_id
  )
  values (
    v_actor_nome,
    v_actor_cpf,
    'Insercao',
    'tb_transacao',
    concat(
      'Produto: ', v_produto.descricao,
      ' | Quantidade: ', p_quantia,
      ' | Pontos: ', v_total,
      ' | Participante: ', p_id_usuario
    ),
    auth.uid()
  );

  return next;
end;
$$;

