drop policy if exists "profiles_select_own_or_staff" on public.tb_usuarios;
create policy "profiles_select_own_or_staff"
on public.tb_usuarios
for select
using (auth_user_id = auth.uid() or public.is_staff());

drop policy if exists "profiles_insert_own_participant" on public.tb_usuarios;
create policy "profiles_insert_own_participant"
on public.tb_usuarios
for insert
with check (auth_user_id = auth.uid() and perfil = 0 and status = 'ativo');

drop policy if exists "profiles_update_own_or_admin" on public.tb_usuarios;
create policy "profiles_update_own_or_admin"
on public.tb_usuarios
for update
using (auth_user_id = auth.uid() or public.is_admin())
with check (
  public.is_admin()
  or (
    auth_user_id = auth.uid()
    and perfil = public.current_perfil()
    and status = 'ativo'
  )
);

drop policy if exists "devices_select_active" on public.tb_dispositivos_eletronicos;
create policy "devices_select_active"
on public.tb_dispositivos_eletronicos
for select
using (status = 'ativo' or public.is_staff());

drop policy if exists "devices_staff_manage" on public.tb_dispositivos_eletronicos;
create policy "devices_staff_manage"
on public.tb_dispositivos_eletronicos
for all
using (public.is_staff())
with check (public.is_staff());

drop policy if exists "transactions_select_own_or_staff" on public.tb_transacao;
create policy "transactions_select_own_or_staff"
on public.tb_transacao
for select
using (id_usuario_fk = public.current_profile_id() or public.is_staff());

drop policy if exists "scores_select_own_or_staff" on public.tb_pontuacao;
create policy "scores_select_own_or_staff"
on public.tb_pontuacao
for select
using (id_usuario_tb_fk = public.current_profile_id() or public.is_staff());

drop policy if exists "audit_admin_select" on public.auditoria;
create policy "audit_admin_select"
on public.auditoria
for select
using (public.is_admin());

